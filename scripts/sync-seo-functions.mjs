/**
 * Cloudflare Pages can fall back to the SPA shell for some root files even when
 * they exist in dist. Per-path function files like `robots.txt.js` do NOT reliably
 * map to `/robots.txt` on Pages.
 *
 * Root `functions/_middleware.js` runs in front of static assets (CF docs) and
 * short-circuits these paths with the real bodies from `dist/`.
 *
 * Run after `astro build` (see package.json "build" script).
 */
import { mkdirSync, readFileSync, writeFileSync, existsSync, unlinkSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const dist = join(root, 'dist');
const fnDir = join(root, 'functions');

/** Remove legacy per-route handlers (they don't bind to dotted paths reliably). */
function removeLegacyFunctionFiles() {
  if (!existsSync(fnDir)) return;
  const legacy = [
    'robots.txt.js',
    'sitemap-index.xml.js',
    'sitemap-0.xml.js',
    'og-image.png.js',
  ];
  for (const name of legacy) {
    const p = join(fnDir, name);
    if (existsSync(p)) unlinkSync(p);
  }
}

if (!existsSync(dist)) {
  console.error('[sync-seo-functions] dist/ missing — run astro build first.');
  process.exit(1);
}

mkdirSync(fnDir, { recursive: true });
removeLegacyFunctionFiles();

const robots = readFileSync(join(dist, 'robots.txt'), 'utf8');
const sitemapIndex = readFileSync(join(dist, 'sitemap-index.xml'), 'utf8');
const sitemap0 = readFileSync(join(dist, 'sitemap-0.xml'), 'utf8');

let pngBlock = '';
const pngPath = join(dist, 'og-image.png');
if (existsSync(pngPath)) {
  const b64 = readFileSync(pngPath).toString('base64');
  const b64lit = JSON.stringify(b64);
  pngBlock = `
  if (path === '/og-image.png') {
    const b64 = ${b64lit};
    const binary = Uint8Array.from(atob(b64), (c) => c.charCodeAt(0));
    return new Response(binary, {
      headers: {
        'Content-Type': 'image/png',
        'Cache-Control': 'public, max-age=86400',
        'X-Content-Type-Options': 'nosniff',
      },
    });
  }
`;
}

const robotsLit = JSON.stringify(robots);
const sitemapIndexLit = JSON.stringify(sitemapIndex);
const sitemap0Lit = JSON.stringify(sitemap0);

const middlewareSrc = `/**
 * Generated by scripts/sync-seo-functions.mjs — do not edit by hand.
 */
export async function onRequest(context) {
  const url = new URL(context.request.url);
  if (context.request.method !== 'GET') {
    return context.next();
  }
  const path = url.pathname;

  if (path === '/robots.txt') {
    return new Response(${robotsLit}, {
      headers: {
        'Content-Type': 'text/plain; charset=utf-8',
        'Cache-Control': 'public, max-age=3600',
        'X-Content-Type-Options': 'nosniff',
      },
    });
  }
  if (path === '/sitemap-index.xml') {
    return new Response(${sitemapIndexLit}, {
      headers: {
        'Content-Type': 'application/xml; charset=utf-8',
        'Cache-Control': 'public, max-age=3600',
        'X-Content-Type-Options': 'nosniff',
      },
    });
  }
  if (path === '/sitemap-0.xml') {
    return new Response(${sitemap0Lit}, {
      headers: {
        'Content-Type': 'application/xml; charset=utf-8',
        'Cache-Control': 'public, max-age=3600',
        'X-Content-Type-Options': 'nosniff',
      },
    });
  }
${pngBlock}
  return context.next();
}
`;

const out = join(fnDir, '_middleware.js');
writeFileSync(out, middlewareSrc, 'utf8');

console.log('[sync-seo-functions] Wrote functions/_middleware.js from dist/.');
